    public String getToolsToRun () {
        String tools = "";
        if (cboxGwaspoly.isSelected())
                tools += "GWASpoly ";
        if (cboxShesis.isSelected())
                tools += "SHEsis ";
        if (cboxPlink.isSelected())
                tools += "PLINK ";
        if (cboxTassel.isSelected())
                tools += "TASSEL ";
        return (tools);
    }
    
    public String getGeneAction () {
        String selection = this.buttonGroupGeneAction.getSelection().getActionCommand();
        return (selection);
    }

